let baseMaps = {
    "Streets": streets,
    "Satellite Streets": satelliteStreets,
};

let myStyle = {
    color: "blue",
    fillColor: "yellow",
    weight: 1
}

// Create the map object with a center and zoom level.
// mapid references the html DIV id for our map object.
// Set view contains the center coordinates, and 4 is the level of zoom.
let map = L.map('mapid', {
    center: [43.7, -79.3], 
    zoom: 11,
    layers: [streets]
    });

// Pass our map layers into our layers control and add the layers control to the map.
L.control.layers(baseMaps).addTo(map);

// Accessing the airport GeoJSON URL
let torontotHoods = "https://raw.githubusercontent.com/lstanczyk90/Mapping_Earthquakes/main/torontoNeighborhoods.json";

// Grabbing our GeoJSON data.
d3.json(torontotHoods).then(function(data) {
    console.log(data)
  // Creating a GeoJSON layer with the retrieved data.
  L.geoJson(data, {
      style: myStyle,
    onEachFeature: function(feature,layer) {
        console.log(layer);
        layer.bindPopup("<h2> Neighborhood: " + feature.properties.AREA_NAME + "</h2>");
    }
}).addTo(map);
})
